# Tipos primitivo

n = input('Digite algo: ')
print(n.isnumeric()) # isnumeric -> Se o valor digitado for número ele mostra 'true', se for uma string ele mostra 'false'
print (n.isalpha()) # isalpha -> Se o valor digitado for string ele mostra 'true', se for um número ele mostra 'false'
print(n.isalnum()) # isalnum -> Mostra se o valor digitado for alfanumérico através de 'true or false'. Ex: 4g.
